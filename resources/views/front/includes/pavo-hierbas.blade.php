          <h2 class="text-center"><img src="images/resources-receta-vales/icon-ingredientes.svg" class="img-responsive icon-plate-cont text-center"/ alt="icono preparac+on receta">Ingredientes</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <ul>
              <li>1 pavo San Fernando 8 – 10kg.</li>
              <li>1 taza de vino blanco.</li>
              <li>½ taza de vinagre.</li>
              <li>5 cucharadas de sal.</li>
              <li>3 cucharadas de ajo molido.</li>
              <li>2 cucharadas de mostaza.</li>
              <li>2 cucharadas de sillao.</li>
              <li>1 rama de huacatay.</li>
              <li>1 rama de culantro.</li>
              <li>1 rama de orégano fresco.</li>
              <li>300 g de tocino en lonjas.</li>
              <li>1 cucharada de chuño.</li>
              <li>2 limones.</li>
            </ul>
          </div>
        </div>
      </div>
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-preparation.svg" class="img-responsive icon-plate-cont text-center"/ alt="icono preparac+on receta">Preparación</h2>
        </div>
        <ul>
          <li>Frotar el pavo San Fernando con limón.</li>
          <li>Mezclar el vino, vinagre y los condimentos.</li>
          <li>Untar el pavo con el aderezo preparado.</li>
          <li>Cubrir al pavo con el culantro, el huacatay y tocino.</li>
          <li>Hornear a 350ºF o 175ºC fuego medio en un tiempo aproximado de 20 minutos por kilo.</li>
          <li>Retirar las hierbas y el tocino del pavo y hervir con el jugo de la fuente y espesar con chuño. Se puede agregar un chorrito de leche o crema y hierbas frescas picadas.</li>
          <li>Hervir con la salsa y espesar con chuño. Se puede agregar un chorrito de leche o crema y hierbas frescas picadas.</li>
          <li>Para cortarlo, esperar 15 minutos luego de que esté listo.</li>
        </ul>
