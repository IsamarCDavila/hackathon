    <div class="row row-nomargin recipe-container">
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-ingredientes.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Ingredientes</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>Pavo San Fernando 8-10g.</li>
              <li>4 cdas de sal.</li>
              <li>1 cda de pimienta.</li>
              <li>1 cda de comino.</li>
              <li>½ de huacatay.</li>
              <li>1 cda de rocoto.</li>
              <li>1 litro de chicha de jora.</li>
              <li>1 taza de vino tinto.</li>
              <li>2 cdas de ajo.</li>
              <li>2 cdas de ají amarillo.</li>
              <li>2 cdas de ají panca.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el relleno:</p>
            <ul>
              <li>½ kg carne de res.</li>
              <li>½ kg carne de cerdo.</li>
              <li>¾ kg cebolla picada.</li>
              <li>1 cda de ajos.</li>
              <li>1 cda de ají panca.</li>
              <li>1 cda de ají amarillo.</li>
              <li>100 g pasas negras.</li>
              <li>100 g aceitunas.</li>
              <li>3 cdas de aceite.</li>
              <li>Sal y pimienta.</li>
              <li>1 cda de maní o pecanas molidas.</li>
              <li>100 g de galleta de vainilla molidas.</li>
            </ul>
          </div>
        </div>
      </div>
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-preparation.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Preparación</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>Freír el ajo y ajíes, agregar el huacatay y sazonar.</li>
              <li>Incorporar la chicha de jora y el vino, hervir y dejar enfriar.</li>
              <li>Macerar el pavo en refrigeración durante 12 horas.</li>
              <li>Hornear a fuego medio (175ºC o 350ºF), aproximadamente 20 minutos por kg de pavo.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el relleno:</p>
            <ul>
              <li>Freír las carnes picadas en cubos.</li>
              <li>Freír cebolla, ajos y ajíes.</li>
              <li>Sazonar y agregar galletas, pasas, aceituna y maní.</li>
              <li>Mezclar bien y rellenar el pavo con esta preparación.</li>
            </ul>
          </div>
