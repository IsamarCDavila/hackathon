    <div class="row row-nomargin recipe-container">
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-ingredientes.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Ingredientes</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>Pavo San Fernando 8-10g.</li>
              <li>3 tazas de chicha de jora.</li>
              <li>5 cucharadas de sal.</li>
              <li>5 cucharadas de sal.</li>
              <li>5 cucharadas de ajo molido.</li>
              <li>2 cucharadas de comino.</li>
              <li>2 cucharadas de pimienta.</li>
              <li>4 cucharadas de ají mirasol molido.</li>
              <li>1 ramito de culantro.</li>
              <li>100 g zapallo loche.</li>
              <li>1 cucharada de chuño.</li>
              <li>Una cabeza de cebolla.</li>
              <li>Una cabeza de ajo.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el acompañamiento:</p>
            <ul>
              <li>1 kg choclo serrano.</li>
              <li>250 g queso fresco.</li>
              <li>100 g queso parmesano.</li>
              <li>1 cebolla picada.</li>
              <li>1 cucharada de ajo molido.</li>
              <li>1 cucharada de ají amarillo molido.</li>
              <li>2 tazas de caldo.</li>
              <li>Cucharada de mantequilla.</li>
              <li>2 cucharadas de polenta.</li>
            </ul>
          </div>
        </div>
      </div>
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-preparation.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Preparación</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>Licuar el zapallo con la chicha de jora y sazonar con sal, ajo, comino, pimienta y ají mirasol. Untar el pavo San Fernando con la mezcla por dentro y por fuera.</li>
              <li>Macerar el pavo por 12 horas en refrigeración.</li>
              <li>Hornear el pavo a 350ºF o 175ºC, aproximadamente 20 minutos por kilo, con chicha de jora, ramas de culantro y cabezas de cebolla y ajo.</li>
              <li>Colar el jugo del horneado y ligar la salsa con un poco de chuño.</li>
              <li>Servir acompañado del pepián rústico gratinado.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el acompañamiento:</p>
            <ul>
              <li>Freír en mantequilla la cebolla, ajos y ají. Sazonar con sal y pimienta.</li>
              <li>Agregar el caldo y la polenta y dejar hervir.</li>
              <li>Incorporar el choclo licuado y mover constantemente por 15 minutos.</li>
              <li>Una vez cocido espolvorear con culantro, queso parmesano y queso fresco desmenuzado.</li>
              <li>Se puede llevar al horno a gratinar por 10 minutos a 350ºF o 150ºC.</li>
            </ul>
          </div>
        </div>
