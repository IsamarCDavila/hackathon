        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-ingredientes.svg" class="img-responsive icon-plate-cont text-center"/ alt="icono preparac+on receta">Ingredientes</h2>
        </div>
        <ul>
          <li>1 pavo San Fernando de 8 a 10 kg.</li>
        </ul>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el aderezo:</p>
            <ul>
              <li>5 cdas. de sal</li>
              <li>2 cdas. de ajo molido</li>
              <li>2 cdas. de pimienta</li>
              <li>2 cdas. de comino</li>
              <li>2 cdas. de orégano</li>
              <li>2 cdas. de ají amarillo molido</li>
              <li>2 cdas. de ají panca molido</li>
              <li>2 cdas. de ají mirasol molido</li>
              <li>½ taza de vinagre</li>
              <li>2 cdas. de azúcar rubia</li>
              <li>Aceite</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el horneado:</p>
            <ul>
              <li>¼ de taza de cerveza</li>
              <li>¼ de taza de vino</li>
              <li>1 cabeza de cebolla</li>
              <li>1 zanahoria en trozos</li>
              <li>1 cabeza de ajo</li>
              <li>1 poro</li>
            </ul>
            <p>Para la salsa:</p>
            <ul>
              <li>1 cda. de chuño</li>
            </ul>
          </div>
        </div>

      </div>
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-preparation.svg" class="img-responsive icon-plate-cont text-center"/ alt="icono preparac+on receta">Preparación</h2>
        </div>
        <ul>
          <li>Fríe en aceite los condimentos del aderezo y luego déjalos enfriar.</li>
          <li>Unta el pavo San Fernando con la mezcla obtenida y déjalo macerar por 12 horas en la refrigeradora.</li>
          <li>Coloca tu pavo en la fuente junto con cerveza o vino,la cebolla, el ajo, la zanahoria y el poro.</li>
          <li> Hornea a 350ºF o 175ºC durante 20 minutos por kilo aproximadamente.</li>
          <li>Para la salsa, cuela el jugo del horneado, luego agrégale el chuño disuelto en agua y ponlo a hervir hasta que esté <br class="hidden-xs"> espeso.</li>
          <li>Para cortarlo, espera 15 minutos luego de que esté listo.</li>
        </ul>
