    <div class="row row-nomargin recipe-container">
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-ingredientes.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Ingredientes</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>1 pavo San Fernando de 8kg.</li>
              <li>4 cdas de sal.</li>
              <li>1 cda de pimienta.</li>
              <li>1 cda de comino.</li>
              <li>½ de huacatay.</li>
              <li>1 cda de rocoto.</li>
              <li>1 litro de chicha de jora.</li>
              <li>1 taza de vino tinto.</li>
              <li>2 cdas de ajo.</li>
              <li>2 cdas de ají amarillo.</li>
              <li>2 cdas de ají panca.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el arroz chaufa (Relleno):</p>
            <ul>
              <li>½ kg de arroz.</li>
              <li>½ kg de chancho asado.</li>
              <li>1 atado de cebolla china.</li>
              <li>2 cucharadas de kión rallado.</li>
              <li>1 cucharada de ajo molido.</li>
              <li>6 cucharadas de sillao.</li>
              <li>2 cucharadas de azúcar rubia.</li>
              <li>2 cucharaditas aceite de ajonjolí.</li>
              <li>2 cucharaditas de canela china.</li>
              <li>1 cucharada de salsa de ostiones.</li>
              <li>4 huevos.</li>
          </div>
        </div>
      </div>
      <div class="recipe-content">
        <div class="title-recipe">
          <h2 class="text-center"><img src="images/resources-receta-vales/icon-preparation.svg" class="img-responsive icon-plate-cont text-center" alt="icono preparac+on receta">Preparación</h2>
        </div>
        <div class="row row-nomargin">
          <div class="frame-dressing">
            <p>Para el pavo:</p>
            <ul>
              <li>Mezclar el condimento.</li>
              <li>Aderezar el pavo San Fernando por dentro y por fuera para macerar por 12 horas en refrigeración.</li>
            </ul>
          </div>
          <div class="frame-baked">
            <p>Para el arroz chaufa (Relleno):</p>
            <ul>
              <li>Cocer el arroz sin sal.</li>
              <li>Hacer un aderezo con los ajos, kión, la salsa de ostiones, cebolla china picada y el azúcar.</li>
              <li>Incorporar el chancho asado en trozos, sazonar con canela china y aceite de ajonjolí.</li>
              <li>Freír el arroz de a pocos, incorporando el aderezo y el sillao.</li>
              <li>Rellenar el pavo con el arroz chaufa. Colocar en la fuente del horneado los trozos de kion y caldo.</li>
              <li>Hornear a 350ºF o 175ºC (Fuego medio), aproximadamente 20 minutos por kilo.</li>
              <li>Retirar la fuente del horno, ligar la salsa con un poco de chuño.</li>
              <li>Rociar el pavo y servir.</li>
            </ul>
          </div>
